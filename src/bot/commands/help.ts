import type { BotContext } from "../context.ts";

export async function helpCommand(ctx: BotContext): Promise<void> {
  await ctx.reply(
    `*Команди бота Таємний Санта*

/start - Головне меню
/create - Створити нову групу
/join <код> - Приєднатися за кодом запрошення
/mygroups - Мої групи
/wishlist - Керувати списком бажань
/draw - (Організатор) Провести жеребкування
/reveal - Переглянути своє призначення
/help - Показати цю довідку

*Як це працює:*
1. Створіть групу та поділіться кодом запрошення
2. Друзі приєднуються за кодом
3. Кожен додає ідеї подарунків у список бажань (за бажанням)
4. Організатор проводить жеребкування
5. Кожен отримує приватне повідомлення зі своїм призначенням`,
    { parse_mode: "Markdown" }
  );
}
